void	ft_rev_int_tab(int *tab, int size)
{
	int	temp;
	int	j;
	int	i;

	j = size - 1;
	i = 0;
	while (i < size / 2)
	{
		temp = tab[i];
		tab[i] = tab[j];
		tab[j] = temp;
		i++;
		j--;
	}
}
