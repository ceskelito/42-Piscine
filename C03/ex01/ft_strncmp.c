int	ft_strncmp(char *s1, char *s2, unsigned int n)
{
	int	i;

	i = 0;
	if ((int)n < 1)
		return (0);
	while (i < (int)n - 1)
	{
		if (*s1 && (*s1 == *s2))
			s1++;
		else
			break ;
		s2++;
		i++;
	}
	return (*s1 - *s2);
}
